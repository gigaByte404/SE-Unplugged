						<h3>Methodology</h3>
						<h4>Learning</h4>
						<p>Learning proper spelling by rote is a traditional element of elementary education and divergence from standard spelling is often perceived as an indicator of low intelligence, illiteracy, or lower class standing.</p>
						<p>Spelling tests are commonly used to assess a student's mastery over the words in the spelling lessons the student has received so far. They can also be an effective practice method. Spelling bees are competitions to determine the best speller of a group. Prominent spelling bees are even televised, such as the National Spelling Bee in the United States.</p>
						<h4>Alteration </h4>
						<p> Divergent spelling is a popular advertising technique, used to attract attention or to render a trademark "suggestive" rather than "merely descriptive." The pastry chains Dunkin' Donuts and Krispy Kreme, for example, employ non-standard spellings.</p>
						<h4>Misspellings</h4>
						<p> While some words admit multiple spellings, some spellings are not considered standard, and thus labeled as misspellings. A misspelled word can be a series of letters that represents no correctly spelled word of the same language at all (such as "leik" for "like") or a correct spelling of another word (such as writing "here" when one means "hear", or "no" when one means "know"). Misspellings of the latter type can easily make their way into printed material because they are not caught by simple computerized spell checkers.</p>

