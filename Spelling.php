						<p>Uniformity in the spelling of words is one of the features of a standard language in modern times, and official languages usually have standard spellings. However, this is a relatively recent development linked to the compiling of dictionaries, the founding of national academies and other institutions of language maintenance, including widespread education and literacy, and often doesn't apply to minority and regional languages.</p>

