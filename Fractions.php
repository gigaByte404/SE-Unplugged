						<h3>Vocabulary</h3>
						<p>In a fraction, the number of equal parts being described is the numerator (from Latin numerātor, "counter" or "numberer"), and the type or variety of the parts is the denominator (from Latin dēnōminātor, "thing that names or designates"). As an example, the fraction ​8⁄5 amounts to eight parts, each of which is of the type named "fifth". In terms of division, the numerator corresponds to the dividend, and the denominator corresponds to the divisor.</p>

						<p>Informally, the numerator and denominator may be distinguished by placement alone but in formal contexts they are always separated by a fraction bar. The fraction bar may be horizontal (as in 1/3), oblique (as in 1/5), or diagonal (as in ​1⁄9). These marks are respectively known as the horizontal bar, the slash (US) or stroke (UK), the division slash, and the fraction slash.[n 1] In typography, horizontal fractions are also known as "en" or "nut fractions" and diagonal fractions as "em fractions", based on the width of a line they take up.</p>
